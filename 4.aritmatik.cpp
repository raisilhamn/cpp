#include <iostream>

using namespace std;

int main(int argc, char const *argv[])
{
    //  !! Perkalian "*"
    int x = 4 * 3;
    cout << x << endl;

    // !! modulo "%"
    int y = 4 % 3;
    cout << y << endl;

    // !! pembagian  "/"
    int z = 10 / 3;
    cout << z << endl;
    // !! C++ menerapkan sifat matematika asosiasi
    return 0;
}
